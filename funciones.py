import math

def circulo(MEDIDA1):		#define la función del área del círculo
    return math.pi * MEDIDA1**2

def rectangulo(MEDIDA1, MEDIDA2):	#define la función del área del rectángulo
    return MEDIDA1 * MEDIDA2

def triangulo(MEDIDA1, MEDIDA2):	#define la función del área del triángulo
    return (MEDIDA1 * MEDIDA2) / 2
